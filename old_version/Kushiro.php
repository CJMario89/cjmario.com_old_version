	<div class="P_title">幸福町</div>
	<p class="Content">雖然那時候都不太怎麼熟，說實在的那時候滿腦子在想怎麼考上資工所，完全不會社交，現在想起來覺得自己真封閉，不過，當時靜靜的看著朋友們開心得鬥嘴、追逐，就感到很快樂了，自己也第一次把雪拿在手中捏，好冰! 也許就是從這時候意識到，我不能滿腦子都是這些東西，也該培養其他能力</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro10.jpg" height="100%" width="100%">
	<div class="P_title">幸福町</div>
	<p class="Content">過了幾個小時，辛苦的三位駕駛終於開到釧路，記得中途好幾次車子打滑，駕駛都嚇到了，如果當初我有開的話，或許還記得開車打滑時的手感</p>

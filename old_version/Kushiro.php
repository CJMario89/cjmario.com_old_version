	<p class="Content">這是在帯広很有名的豬排丼飯，印象中醬很香</p>
	
	<img class="Picture" id="Kushiro" src="pictures/Kushiro7.jpg" height="100%" width="100%">
	<div class="P_title">幸福町</div>
	<p class="Content">因為平常的我真的很邊緣，所以這時候我應該在想有朋友一起拍照真好，而且阿眼前風景又那麼漂亮，不愧是在幸福町</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro8.jpg" height="100%" width="100%">

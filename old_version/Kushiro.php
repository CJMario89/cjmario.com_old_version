	<div class="P_title">ヤマダデンキ</div>
	<p class="Content">當初為了能在飯店玩哥帶的Switch(因為手把不夠)所以跑到附近的遊戲商店買，先跑到ヤマダデンキ，再跑到5公里遠的Toys"R"Us才買到手把XD 還記得當初我整張臉因為天氣非常冷，室內暖氣很熱，我的臉刺激得又癢又痛，現在才知道那是因為溫度上升時，會讓臉部水分流失更快，進而發炎，現在在台灣用Cetaphil乳液就能有效控制發炎~推薦，我想有了它之後去北海道應該就沒問題了</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro19.jpg" height="100%" width="100%">
	<div class="P_title">末広町</div>
	<p class="Content">這附近很多啤酒喝到飽，也是我學到飲み放題這個單字的時候，本來要去喝的，忘記什麼原因沒喝(不過我心裡是反對的，因為我那時在控制體重)</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro20.jpg" height="100%" width="100%">
	<div class="P_title">泉屋 スパカツ</div>
	<p class="Content">後來晚餐找到一家在釧路很有名的洋食老店~泉屋~，它的義大利麵份量超巨，控制體重的我自然就剩很多，底下的麵有一種鍋巴的感覺~</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro17.jpg" height="100%" width="100%">
	<div class="P_title">天然温泉幣舞の湯ラビスタ</div>
	<p class="Content">吃完飯大家一起回飯店玩switch，這家飯店的視野超棒，而且室內格局好、乾淨，重點價格非常便宜，記得還有免費的拉麵可以吃，只是沒去吃，感謝朋友找到那麼讚的飯店。</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro21.jpg" height="100%" width="100%">
	<div class="P_title">和商市場 大內商店</div>
	<p class="Content">隔天早上直接到魚市場吃有名的勝手丼，依照自己的喜好任意挑選生魚片的組合，每種都有夠新鮮有鮮味好吃，印象中有朋友買綠色的魚卵，看到朋友吃時的表情我不敢嘗試www</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro6.jpg" height="100%" width="100%">
	<div class="P_title">釧路濕原展望台</div>
	<p class="Content">在前往阿寒湖的路上到了一個濕原展望台，感覺開始有點冷，雪有點厚大概可以蓋到腳踝，這裡像是個博物館，可以買票上去參觀更多，然後到屋頂展望，從屋頂看上去是一片雪和一堆樹林，然後更冷...</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro4.jpg" height="100%" width="100%">
	<div class="P_title">釧路濕原展望台</div>
	<p class="Content">裡面吸引到我的就是黃金神威的應援海報看板，我一直在想如果能把所有有應援的地點集齊就太棒了，有點像聖地巡禮，但沒有要到網走w，下次吧!</p>
	<img class="Picture" id="Kushiro" src="pictures/Kushiro5.jpg" height="100%" width="100%">
	<div class="P_title">釧路濕原展望台</div>
	<p class="Content">到了阿寒湖周邊開始下起小雪，只能說冷到爆炸，一直發抖，全身用力抖，還是止不住冷，一生中經歷過最冷的時刻，沒有帶羽絨和毛衣，我想說衣服疊一疊應該夠暖了，我錯了，而且湖旁邊的風一直吹，耳朵凍到真的痛...</p>

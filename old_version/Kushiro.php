	<p class="Content">從冰上鑿一個洞再搭起帳棚釣魚，有些比較大的多人釣魚場還有暖氣，因為這次沒有想說要待很久，所以沒有釣魚，下次要住在湖的旁邊，來這裡釣魚，順便到網走瞭解愛奴文化~</p>



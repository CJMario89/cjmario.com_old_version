	<div class="P_title">阿寒湖</div>
	<p class="Content">前面是一大片冰結得很厚的湖面，人們都在上面進行湖面上的活動，最主要應該是釣魚，甚至停了一大堆轎車都沒問題，除了驚訝之外，就是更冷的感覺</p>

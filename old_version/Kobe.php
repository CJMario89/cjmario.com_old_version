	<p class="Content">到達美利堅公園前我就發現了我的雙腳快炸開，因為我一直赤腳穿著硬底的木板鞋在神戶逛，在每踩一步就痛到不行的情況下，勉強到達美利堅公園後坐在板凳椅上休息了一陣子，哥哥也等了我好一陣子🙏，最終我脫下鞋子赤腳踩著格盤狀的磚頭地板，邊甩著剛買到的背包邊旋轉前進，減少疼痛。</p>

	<p class="Content">在三宮車站附近的商店街逛了一個下午，還記得那幾天手邊隨時都要有一瓶紅茶花伝的水蜜桃紅茶，因為實在太香甜又順口解膩了，結果到一家咖啡廳休息時還吃了水蜜桃口味的甜點，完全是水蜜桃中毒(更誇張的是現在還愛上了水蜜桃口味的歌，只差香水了(X。</p>
	<img class="Picture" id="Kobe7" src="pictures/Kobe7.jpg" height="100%" width="100%">
	<div class="P_title">Excelsior caffe 水蜜桃甜點</div>
	

	<p class="Content">身為拉麵愛好者，到達神戶第一件事就是去吃神戶牛拉麵，中午直接到達這家八坐和拉麵，雖然味道不太記得（當時是這樣說的：湯中有毫不掩飾的甜味，還有牧場的味道，麵有彈性，旁邊壽司的肉比較好吃（現在還記得中間那格最好吃</p>
	<img class="Picture" id="Kobe7" src="pictures/Kobe11.jpg" height="100%" width="100%">
	<div class="P_title">八坐和 神戸牛ラーメンⅹ塩</div>


	<p class="Content">在三宮商店街還有一個目標就是尋找新的背包，一直以來我都揹著黑色功能型的大背包，而我一直想要一個褐色有設計感的背包，剛好找到了一家背包專賣店買了anello的背包，非常滿意，換上新的背包就前往了神戶的夜景點～美利堅公園～</p>
	<img class="Picture" id="Kobe2" src="pictures/Kobe2.jpg" height="100%" width="100%">
